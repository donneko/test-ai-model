// ====== タグモデル用データ自動生成ユーティリティ ======
// 入力: 日本語の自然文（複数行OK）
// 出力: const data = { ... } のコード文字列（コピペ用）
//
// 特徴：
// 1) Intl.Segmenter('ja',{granularity:'word'}) で単語切り出し
// 2) 簡易ルールで token -> tag を推定
// 3) 実際の出現遷移から next_tag を集計（後続トークンの tag を集約）
// 4) コピペできる整形済み JS を出力
//
// ※ Intl.Segmenter が無い環境では単純なフォールバック分割に切り替え

function generateTagDataCode(input) {
  const tokens = tokenizeJA(input);
  const uniqueTokens = Array.from(new Set(tokens));

  // 1) 各トークンに tag を付与（ルールベース）
  const tokenTags = new Map();
  for (const tk of uniqueTokens) {
    tokenTags.set(tk, classifyTokenTags(tk));
  }

  // 2) 遷移観測: token[i] -> token[i+1] のタグを next_tag 候補として集計
  const nextTagMap = new Map(); // Map<token, Set<tag>>
  for (let i = 0; i < tokens.length - 1; i++) {
    const from = tokens[i];
    const to = tokens[i + 1];
    const toTags = tokenTags.get(to) ?? classifyTokenTags(to);
    if (!nextTagMap.has(from)) nextTagMap.set(from, new Set());
    for (const tg of toTags) {
      nextTagMap.get(from).add(tg);
    }
  }

  // 3) data オブジェクト構築
  const dataObj = {};
  for (const tk of uniqueTokens) {
    const tagList = Array.from(tokenTags.get(tk) ?? []);
    const nextTagList = Array.from(nextTagMap.get(tk) ?? new Set());

    // 仕上げ: next_tag は、よく使う上位カテゴリ順に整える
    const order = [
      "主語","主題化","述語","動詞","形容","丁寧",
      "話題","名詞","時","接続",
      "終端","終了","区切り","記号","挨拶","文頭"
    ];
    tagList.sort((a,b)=>orderIndex(a,order)-orderIndex(b,order));
    const dedupNext = dedupPreferOrder(nextTagList, order);

    dataObj[tk] = {
      tag: tagList,
      next_tag: dedupNext,
    };
  }

  // 4) コード文字列として整形して返す
  return `// Generated from input
const data = ${stringifyData(dataObj)};`;
}

// ====== 日本語トークン化 ======
function tokenizeJA(text) {
  // Intl.Segmenter があれば使う
  if (typeof Intl !== "undefined" && Intl.Segmenter) {
    const seg = new Intl.Segmenter("ja", { granularity: "word" });
    const out = [];
    for (const { segment, isWordLike } of seg.segment(text)) {
      const s = segment.trim();
      if (!s) continue;
      // 記号・句読点も学習のため残す（「。」「、」「？」など）
      // ただし全角・半角空白は除外
      out.push(s);
    }
    return out;
  }

  // フォールバック: ざっくり分割（句読点・記号で区切る）
  return text
    .replace(/\s+/g, " ")
    .split(/([。、「」、，．。！？!?・…\(\)\[\]{}〈〉《》—ー\-~〜])/)
    .map(s => s.trim())
    .filter(s => s.length > 0);
}

// ====== タグ推定（簡易ルール） ======
function classifyTokenTags(token) {
  const tags = new Set();

  // 挨拶
  const greetings = ["こんにちは","やあ","どうも","やっほー","もしもし","お疲れさま","久しぶり"];
  if (greetings.includes(token)) {
    tags.add("挨拶"); tags.add("文頭");
  }

  // 代名詞・主語候補
  const pronouns = ["私","僕","俺","あなた","君","皆","みんな"];
  if (pronouns.includes(token)) {
    tags.add("主語"); tags.add("名詞");
  }

  // 時間語
  const timeWords = ["今日","今日は","明日","明日は","昨日","昨日は","今","今は","朝","夜","今年","去年","昨年"];
  if (timeWords.includes(token)) {
    tags.add("時"); tags.add("話題"); tags.add("名詞");
  }

  // 接続
  const connectives = ["ところで","しかし","でも","そして","ただ","それで","だから","だが","一方で"];
  if (connectives.includes(token)) {
    tags.add("接続");
  }

  // 助詞 / 主題化
  const particles = ["は","が","を","に","で","と","も","の","へ","や","から","まで","より","って","ね","よ"];
  if (particles.includes(token)) {
    tags.add("助詞");
    if (token === "は") tags.add("主題化");
    if (token === "ね" || token === "よ") tags.add("終端"); // 文末調整に使われがち
  }

  // 文末・丁寧
  const politeEndings = ["です","ます","でした","ですね","でしょう","でしたね"];
  if (politeEndings.includes(token)) {
    tags.add("丁寧");
    if (token.endsWith("ね")) tags.add("終端");
    // 述語性も多いのでフラグ
    tags.add("述語");
  }

  // 形容・述語：簡単な既知語
  const adjectives = ["いい","楽しい","嬉しい","面白い","難しい","微妙","必要","大事","最高","最悪"];
  if (adjectives.includes(token)) {
    tags.add("述語"); tags.add("形容");
  }

  // 動詞っぽさ（超簡易）：〜する / 〜した / 〜して / 〜します
  if (/する$|した$|して$|します$/.test(token)) {
    tags.add("述語"); tags.add("動詞");
  }

  // 句読点・記号
  if (/[。！？?!]/.test(token)) tags.add("終了");
  if (token === "、" || token === "，" || token === "・") tags.add("区切り");
  if (/^[()（）［］【】｛｝\[\]{}〈〉《》…—ー~〜\-]$/.test(token)) tags.add("記号");

  // 未分類で平仮名・カタカナ・漢字が混じる名詞候補は「名詞」「話題」
  if (tags.size === 0) {
    if (/^[\p{sc=Hiragana}\p{sc=Katakana}\p{sc=Han}ー]+$/u.test(token)) {
      tags.add("名詞"); tags.add("話題");
    }
  }

  // 最終保険：何も付いてなければ名詞扱い
  if (tags.size === 0) tags.add("名詞");

  return Array.from(tags);
}

// ====== 整形系ユーティリティ ======
function orderIndex(x, order){ const i = order.indexOf(x); return i === -1 ? 999 : i; }
function dedupPreferOrder(arr, order){
  const set = new Set(arr);
  return Array.from(set).sort((a,b)=>orderIndex(a,order)-orderIndex(b,order));
}

// オブジェクトを読みやすく成形（キー順は日本語ソート）
function stringifyData(obj){
  const sortedKeys = Object.keys(obj).sort((a,b)=>a.localeCompare(b, 'ja'));
  const lines = [];
  lines.push("{");
  for (let i=0;i<sortedKeys.length;i++){
    const k = sortedKeys[i];
    const v = obj[k];

    const tagStr = JSON.stringify(v.tag, null, 0);
    const nextStr = JSON.stringify(v.next_tag, null, 0);

    lines.push(`  ${JSON.stringify(k)}: { tag: ${tagStr}, next_tag: ${nextStr} },`);
  }
  lines.push("}");
  return lines.join("\n");
}


